// FromHexToRGB function takes in a HTML Hex Code color and returns the
// corresponding models.RGBColor. If an error is present the models.RGBColor
// will default to Red=0, Blue=0, Green=0 "Black"
func FromHexToRGB(hexcode string) (models.RGBColor, error) {
	v, err := strconv.ParseUint(hexcode, 16, 32)
	if err != nil {
		return models.RGBColor{}, err
	}
	return models.RGBColor{
		Red:   uint8(v >> 16),
		Green: uint8((v >> 8) & 0xFF),
		Blue:  uint8(v & 0xFF),
	}, nil
}

// RGB function takes 3 uint8 (<=256) that represents a RGB Color
func RGB(r uint8, g uint8, b uint8) models.RGBColor {
	return models.RGBColor{
		Red:   r,
		Green: g,
		Blue:  b,
	}
}

